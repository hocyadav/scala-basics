object MyFirstObject extends App {
  var a = 10;
  println(s"a = ${a}");
  println(a);
  println("hello world");

  val x = 1 + 2;
  println(x + "  - - ")

  val c: Int = 10;
  println(c)

  val str: String = "hariom yadav";
  println(str);

  val ch: Char = 'h';
  println(ch);

  val t: Boolean = true;
  val t2: Boolean = false;
  println(t + " " + t2);
}
